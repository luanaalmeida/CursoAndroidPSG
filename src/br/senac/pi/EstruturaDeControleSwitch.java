package br.senac.pi;

public class EstruturaDeControleSwitch {

	public static void main(String[] args) {
		
		int n = 10;
		
		switch (n){
		case 1:
		System.out.println("O valor da vairave 1");
		break;
		
		case 2:
		System.out.println("O valor da vairave 2");
		break;
		
		case 3:
		System.out.println("O valor da vairave 3");
		break;
		
		case 4:
		System.out.println("O valor da vairave 4");
		break;
		
		default:
			System.out.println("Nenhum Valor na Variável.");
			break;
		}
	}
}
